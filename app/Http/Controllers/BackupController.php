    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'backup' => 'required|file|mimes:txt,pdf', // Valida que sea un archivo .txt o .pdf
        ]);

        $file = $request->file('backup');
        $path = $file->store('backups'); // Guarda el archivo en el directorio 'storage/app/backups'

        $backup = new Backup();
        $backup->archivo = $path; // Guarda la ruta del archivo en la base de datos
        $backup->save();

        return back()->with('success', 'Backup registrado correctamente');
    }

